'use strict';

/**
 * add number
 *
 * ```js
 * addNumber(1,2){
 *  return 1+2
 * }
 * //=> 3
 *
 *
 * @param {Number} `target`
 * @param {Number} `number`
 * @return {Number}
 */

module.exports = function addNumber(target, number) {
  return target + number
};
